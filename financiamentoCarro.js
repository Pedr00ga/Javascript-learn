
function financiamentoCarro (valorTotal, jurosAno = 25, Anos = 5){
    let = valorTotalJuros = 0
    let = valorTotalCarro = 0
    let = jurosTotal = jurosAno * Anos
    valorTotalJuros = (valorTotal * jurosTotal) / 100
    valorTotalCarro = valorTotal + valorTotalJuros
    return valorTotalCarro

}

